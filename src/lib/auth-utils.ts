const WHITESPACE_RE = /\s+/g;

export function sanitizeText(input: string): string {
  return input.replace(WHITESPACE_RE, " ").trim();
}

export function normalizeEmail(input: string): string {
  return sanitizeText(input).toLowerCase();
}

export function looksLikeEmail(input: string): boolean {
  const v = sanitizeText(input);
  // Conservative email check; avoids blocking valid emails.
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(v);
}

export function normalizeUsername(input: string): string {
  return sanitizeText(input);
}

export function usernameMinLengthOk(username: string): boolean {
  return sanitizeText(username).length >= 4;
}

export function passwordPolicyCheck(password: string): {
  ok: boolean;
  issues: string[];
} {
  const issues: string[] = [];
  const pw = password;

  if (pw.length < 8) issues.push("Password must be at least 8 characters.");
  if (!/[A-Z]/.test(pw))
    issues.push("Password must include at least one uppercase letter.");
  if (!/[a-z]/.test(pw))
    issues.push("Password must include at least one lowercase letter.");
  if (!/[0-9]/.test(pw))
    issues.push("Password must include at least one number.");
  if (!/[^A-Za-z0-9]/.test(pw))
    issues.push("Password must include at least one special character.");

  return { ok: issues.length === 0, issues };
}

export function getPasswordStrength(
  password: string,
): "Weak" | "Medium" | "Strong" {
  const pw = password;
  let score = 0;

  if (pw.length >= 8) score += 1;
  if (pw.length >= 12) score += 1;
  if (/[A-Z]/.test(pw)) score += 1;
  if (/[a-z]/.test(pw)) score += 1;
  if (/[0-9]/.test(pw)) score += 1;
  if (/[^A-Za-z0-9]/.test(pw)) score += 1;

  // 0-2 => Weak, 3-4 => Medium, 5+ => Strong
  if (score <= 2) return "Weak";
  if (score <= 4) return "Medium";
  return "Strong";
}

export function mapSupabaseAuthErrorToMessage(error: string): string {
  const e = error.toLowerCase();

  if (e.includes("invalid login credentials") || e.includes("invalid')")) {
    return "Invalid credentials. Please check your email/username and password.";
  }

  if (e.includes("invalid grant") || e.includes("could not")) {
    return "Invalid credentials";
  }

  if (e.includes("email not confirmed") || e.includes("email confirmation")) {
    return "Email verification required. Please check your email to verify your account.";
  }

  if (e.includes("weak password") || e.includes("password too weak")) {
    return "Weak password. Please choose a stronger password.";
  }

  if (
    e.includes("already registered") ||
    e.includes("duplicate") ||
    e.includes("unique")
  ) {
    return "Email already registered. Please sign in or use a different email.";
  }

  if (e.includes("network error") || e.includes("fetch")) {
    return "Network error. Please try again.";
  }

  if (e.includes("rate limit") || e.includes("too many requests")) {
    return "Please slow down and try again.";
  }

  if (e.includes("unexpected")) {
    return "Unexpected error. Please try again.";
  }

  // Fallback
  return error;
}

export function safeReferralCode(referral?: string): string | null {
  const v = referral ? sanitizeText(referral) : "";
  return v ? v : null;
}

